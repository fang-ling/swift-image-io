import CPNG
import Foundation

extension PNGDecoder {
  /**
   * A struct that hold the buffer and its state.
   */
  struct MemoryBuffer {
    /**
     * Pointer to the PNG data.
     */
    var buffer: UnsafeMutablePointer<Int8>

    /**
     * Size of the data.
     */
    var size: Int

    /**
     * Current read offset.
     */
    var offset: Int
  }
}

